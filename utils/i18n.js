function format(template, params = {}) {
    console.log(`üåç DEBUG FORMAT: Template="${template}"`, `Params=`, params);
    const result = template.replace(/\{(\w+)\}/g, (_, k) => {
        const replacement = params[k] ?? '';
        console.log(`üåç DEBUG FORMAT: Substituindo {${k}} -> "${replacement}"`);
        return replacement;
    });
    console.log(`üåç DEBUG FORMAT: Resultado final="${result}"`);
    return result;
}

